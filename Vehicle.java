
public class Vehicle {
	
	
	int id;
	int passenger;
	
	public Vehicle(int id , int passe){
		
		this.id=id;
		passenger=passe;		
		
		
	}
	
	
	public void setId(int id){
		this.id=id;
		
	}
	public int getId(){
		return id;
		
	}
	public void setPassenger(int passenger){
		this.passenger=passenger;
		
	}
	public int getPassenger(){
		return passenger;
		
	}
	
	
	
	
	
	
	

}
